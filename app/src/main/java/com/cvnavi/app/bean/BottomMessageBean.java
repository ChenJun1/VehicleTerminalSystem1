public class BottomMessageBean {

    /**
     * 显示方式
     */
    private int style;

    /**
     * 显示时间
     */
    private int time;

    /**
     * 内容
     */
    private String content;

    public int getStyle() {
        return style;
    }

    public void setStyle(int style) {
        this.style = style;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
