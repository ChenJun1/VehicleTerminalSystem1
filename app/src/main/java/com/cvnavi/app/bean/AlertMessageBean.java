public class AlertMessageBean {

    /**
     * 弹出显示的时间
     */
    private int time;

    /**
     * 弹出内容
     */
    private String content;

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
